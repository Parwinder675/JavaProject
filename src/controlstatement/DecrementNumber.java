package controlstatement;

public class DecrementNumber {

	public static void main(String[] args) {
		
		
		
		//for loop
		for(int i=10;i>=0;i--) {
			
			System.out.println(i);

		}
			System.out.println("..............................");
		
			// do loop
		int k=10;
			while(k>=0) {
				System.out.println(k);
				
				k--;
			}
			System.out.println("..............................");
			
			//do-while loop
			int m=10;
			do {
				System.out.println(m);
				m--;
			}
			while(m>=0);
				
					

	}

}
