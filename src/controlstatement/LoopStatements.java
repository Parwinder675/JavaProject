package controlstatement;

public class LoopStatements {

	public static void main(String[] args) {
		
		//for loop
		for(int i=0;i<=10;i++) {
		
		System.out.println(i);


	}
		System.out.println("..............................");
		
		
		// while loop
	int k=0;
		while(k<=10) {
			System.out.println(k);
			
			k++;
		}
		System.out.println("..............................");
		
		//do-while loop
		int m=0;
		do {
			System.out.println(m);
			m++;
		}
		while(m<=10);
			
		
		
		
		
		
}	
	
}
